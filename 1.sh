#!/bin/bash

# ==================================================
# 🚀 一键提交并推送（简化版）
# 仅适用于确认当前是 Git 仓库的环境
# 提交信息固定为 "1"
# ==================================================

echo "🚀 正在一键提交并推送..."

# 1. 添加所有修改和新文件（不包括 .gitignore 中的）
git add .

# 2. 提交更改
git commit -m "1"

# 3. 获取当前分支名
BRANCH=$(git symbolic-ref --short HEAD)

# 4. 推送到 origin（自动设置上游）
git push -u origin $BRANCH

echo "✅ 提交并推送成功！提交信息: \"1\""
echo "📌 当前分支: $BRANCH"